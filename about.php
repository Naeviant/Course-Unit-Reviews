<h1>About This Website</h1>
<p>
    Choosing your course units is hard. You may be looking at the list and be dreading a compulsory module. Perhaps you've chosen all the units that you want to do, but haven't picked enough credits yet and still need to choose another one or two.
    Don't worry. In our experience, some of the course units we thought we would really dislike ended up being some of the best. Equally, some of the course units we were really looking forward to ended up being the ones we didn't particularly enjoy.
    The purpose of this website is to capture the thoughts of people who have already done the course units and publish them to other students. We hope that this will allow people to make informed decisions about their course unit choices and avoid any horrible surprises!
</p>
<p>
    Data about course units for 2020-2021 has been pulled from the StudentNet. The system will automatically identify what components make up the course unit (exams, lectures, tutorials, etc.). Each reviewer will be asked to rate each of the following components (where applicable):
</p>
<ul>
    <li>Exams</li>
    <li>Coursework</li>
    <li>Lectures</li>
    <li>Labs/Workshops</li>
    <li>Tutorials</li>
</ul>
<p>
    Each component is given a rating out of five stars. The reviewer also has the option of adding some text to explain why they chose that rating. Furthermore, they also have the option to add any further information about the course unit not already included in the review.</p>
<p>
    Readers who are logged into the site can indicate whether or not they agree with a review using the relevant buttons on the webpage. These help to build an <em>Agreement Score</em> which shows how reliable a review is. Reviews with a negative agreement score have been judged to be inaccurate. Reviews with a positive agreement score have been judged to be very accurate. Reviews with an agreement score of 0 should not be treated as reliable or unreliable.
</p>
